



var displayDAy = function (dayNUm){
    switch (dayNUm){
        case 1:
            console.log(`monday`);
            break;
            case 2:
                console.log(`Tuesday`);
                break;
                case 3:
                console.log(`Wednesday`);
                break;
                case 4:
                console.log(`Thursday`);
                break;
                case 5:
                console.log(`friday`);
                break;
                case 6:
                console.log(`Saturday`);
                break;
                case 7:
                console.log(`sunday`);
                break;
                default:
                    console.log(`invalid: ${dayNUm}`);
                break;

                
    }
};

